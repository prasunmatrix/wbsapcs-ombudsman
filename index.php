            <p>(1) In any legal proceeding where a court, pursuant to clause (a) of sub-section (1) of section 34
              of the Act, directs on an application made by a protected person or any other person, that ln the
              interests of justice the proceeding or any part thereof be conducted by suppressing the identity of
              such protected person, the Registrar of the court shall direct all parties involved to:-
            </p>
            <p>(i) file one copy of the documents bearing the full name, identity and identifying details of the
              parties concerned before the court, which shall be kept in a sealed cover and in safe custody with
              the Registrar; and
            </p>
            <p>(ii) Serve one copy of documents bearing the full name, identity and identifying details of the
              parties concerned upon other parties in the proceeding with a requirement to ensure that full name
              and identity of the parties concerned are kept confidential.
            </p>
            <p>(2) The Registrar shall provide pseudonyms to protected persons involved in the legal proceedings
              in the documents, filed before the court in such manner that the identity and identifying details
              of the protected person involved in the legal proceeding are kept confidential.
            </p>
            <p>(3) The Registrar shall place the sealed covered documents before the court on the first date the
              legal proceeding is listed for hearing before the court, if so required by the court.
            </p>
            <p>(4) The identities of the protected person involved in the legal proceeding and their identifying
              details shall be displayed in pseudonym in all documentation generated by the court in relation to
              the legal proceeding, including listing of the case on the Court Board, interim orders and final
              judgments.
            </p>
            <p>(5) The identity and identifying details of the protected person involved in the legal proceeding
              shall not be revealed by any person or their representatives including assistants and staff except
              where, in the interest of justice, the name and identify of the protected person needs to be
              revealed to a third party, it shall only be allowed by an order of court.
            </p>
            <p>(6) Printing or publishing any matter in relation to the aforementioned legal proceedings in
              electronic or any other form, shall be lawful only if the same is done by ensuring the suppression
