            </p>
            <p>f) The Ombudsman shall have the power to pass orders, including withdrawal and rectiflcation of the
              violation, counseling, social service etc;
            </p>
            <p>g) The Ombudsman shall inform the complaint of the action taken; and</p>
            <p>h) The Ombudsman shall inform the parties to the complaint of their right to seek judicial review
              from the Ombudsman's order;
            </p>
