              violation took place, within three months from the date that the person making the complaint became
              aware of the alleged violation of the Act;
            </p>
            <p>Provided that the Ombudsman may, for reasons to be recorded in writing, extend the time limit to
              make the complaint by a further period of three months, if he is satisfied that circumstances
              prevented the complaint from making the complaint with the stipulated period;
            </p>
            <p>(2) All complaints shall be made to the Ombudsman I writing in accordance with the form set out in
              the Appendix to these rules:
            </p>
