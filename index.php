    <section id="act" class="about">
      <div class="container" data-aos="fade-up">
        <div class="section-title">
          <h2>HIV/AIDS Act</h2>
        </div>
        <div class="row content">
          <div class="col-lg-12">
            <p><a href="assets/documents/HIV_AIDS_Act_2017.pdf" target="_blank"><strong>Copy of the HIV and AIDS Act, 2017/major provisions of the HIV and AIDS Act, 2017</strong></a></p>
            <p><a href="assets/documents/Notification_Of_HIVAIDS_ACT.pdf" target="_blank"><strong>The West Bengal Human Immunodeficiency Virus & Acquired Immune Deficiency Syndrome (Ombudsman & Legal Proceedings) rules, 2021 </strong></a></p>
            <h3>Rights of protected people mentioned under the Act.</h3>
            <br>
            <p><strong>RIGHT TO TREATMENT:</strong> A person suffering from any ailment has the right to get treatment for his suffering. Treatment cannot be denied to a patient on the basis of his HIV/AIDS status. If any HIV/AIDS patient is denied treatment, it amounts to discrimination. The Supreme Court of India has issued directions to make second-line HIV/AIDS treatment available free of cost to all those who need it.</p>
            <p>In the matter of LX v. Union of India, LX, an under trial who was tested HIV+ was denied antiviral therapy (ART) against AIDS after his release form the prison. In a petition filed by him, the Delhi High Court directed the Government to continue to provide ART to LX. Pursuant to the commencement of the ARV roll out by the Government of India in April 2004, the High Court directed the Government provide ART to LX under the ARV roll-out programme and to reimburse AIIMS for the costs incurred by them.</p>
            <p>In another matter a pregnant lady infected with HIV+ was denied treatment by the hospital. In a petition filed by her husband, the Delhi High Court gave immediate directions to the Hospital saying that there was an urgent need to issue direction to ensure the protection of the Right to Health and Life of the women and her foetus. A direction was issued to immediately arrange one unit of Blood and further quantity of Blood as may be required, from any of the authorized Blood Banks.</p>
            <p><strong>CONFIDENTIALITY:</strong> A person who has been diagnosed with HIV/AIDS has the right to keep his/her HIV/AIDS status confidential. Even the Courts have delivered judgments in their favour that if they do not want to disclose their identity they can use a pseudonym before the Courts to suppress their identity.</p>
            <p>In the matter of Mr. X v. Hospital Z, the Supreme Court of India has held that the patients suffering from 'AIDS' deserve full sympathy and are entitled to all respect as human beings. They cannot be denied jobs on the basis of their HIV/AIDS status. It further held that although the doctor patient confidentiality is an important and part of the medical ethics incorporated by the then Medical Council Act, a patient's right to confidentiality was not enforceable in a situation where the patient is HIV positive, if he stood the risk of spreading it to his prospective spouse. Since acts likely to spread communicable diseases are a crime, the failure of the hospital to inform the spouse of the disease would make them participant criminals. Since Indian matrimonial laws provide venereal disease as a ground for divorce, a person suffering from a VD had no right to get married till s/he/ is fully cured and such a right must be treated as a 'suspended right'.</p>
            <p>In another case of Mr. X v Hospital Z, the Supreme Court has held that its earlier judgment in Mr. X v Hospital Z to the extent that it suspended the right of people living with HIV/AIDS to marry is no longer good law and restored the right of an HIV + person to marry. However, it further held that this does not take away from the duty of those who know their HIV+ status to obtain informed consent from their prospective spouse prior to marriage.</p>
            <p><strong>RIGHT TO EMPLOYMENT AND RIGHT AGAINT DISCRIMINATION AT WORKPLACE:</strong> Right against discrimination is a fundamental right possessed by a citizen of India. No one can be discriminated on the basis of his HIV/AIDS status in India. HIV/AIDS patients have a right of equal treatment everywhere and they cannot denied job opportunity or discriminated in employment matters on the ground of their HIV/IDS status.</p>
            <p>In a landmark judgment in the matter of MX v. ZY, the Bombay High Court has held that no person could be deprived of his or her livelihood except by procedure established by law and that the procedure must be just, fair and reasonable. It further held that held that if a person is fit to perform his job functions and is otherwise qualified and does not pose a substantial risk to fellow workers he cannot be denied the job. Further, the Court held that a public sector employer cannot deny a person employment solely because he is HIV positive. Each determination of whether a person is incapable of performing the job must be made by conducting an individual inquiry taking into account the state of Medical knowledge at the time. It was also held that in proper cases where a person can show that he or she would not be able to prosecute his or her career if his status is disclosed and in the interests of the administration of justice, the Court will permit the party before it to suppress his or her identity and prosecute or defend the proceedings under an assumed name.</p>
            <p>In another case of G v. New India Assurance Co. Ltd, the Bombay High Court has held that a person, who is otherwise fit, could not be denied employment only on the ground that he or she is HIV positive. The Court further held that a personal HIV status cannot be a ground for rejection for employment as it would be discriminatory and would violate of the principles laid down in Articles 14 (Right to Equality), 16 (Right to non-discrimination in state Employment) and 21 (Right to Life) of the Constitution.</p>
            <p>In the case of S v. Director General of Police, where the widow was denied compassionate employment because her husband had died because of AIDS, the Bombay High Court has held that there should be no delay in appointment in all claims of compassionate employment. If there does not exist a suitable post, a supernumerary post must be created. The Court directed the respondents to create a supernumerary post for the petitioner within 8 weeks, and consider her case for grant of service quarters on priority basis in accordance with the rules.</p>
            <p>In the matter of Mr. X, Indian Inhabitant Vs. Chairman, State Level Police Recruitment Board and Ors., the Andhra Pradesh High Court has held that treating all HIV positive persons as one single homogenous class, irrespective of the stage of the disease, for being denied appointment in the police force is in violation of Articles 14 and 16 of the Constitution of India. Some people found to be HIV positive may, under certain circumstances, be unsuitable for employment in the police force does not justify the exclusion from employment of all people who are living with HIV. A Rule which denies employment to the HIV infected person merely on the ground of his HIV status irrespective of his ability to perform the job requirements and irrespective of the fact that he does not pose any threat to others at the workplace is clearly arbitrary and unreasonable and infringes the whole some requirement of Article 14 as well as Article 21 of the Constitution of India.</p>
            <p>The Supreme Court of India has also advocated in the favour of better living for sex workers. It has also directed that the Central and the State Governments should prepare schemes for rehabilitation of physically and sexually abused women all across the Country.</p>
            <h3>Model HIV and AIDS Policy for Establishments</h3>
            <br>
            <p>The appropriate Authority under the Central Government shall, before notifying a model HIV and AIDS policy for establishments Consult – </p>
            <p>(a) All stakeholders including representatives of HIV -Positive Persons;</p>
            <p>(b) HIV -affected Persons and protected Persons;</p>
            <p>(c) Healthcare providers;</p>
            <p>(d) Establishments engaged in providing Education, Healthcare Services, Experts and Organizations working in the field of HIV and AIDS, Employers, Trade Unions, and other relevant Stakeholders on such policy.</p>
            <ul>
              <li><i class="ri-check-double-line"></i> The appropriate authority under the Central Government shall notify a model HIV and AIDS policy for establishments in the Official Gazette. </li>
              <li><i class="ri-check-double-line"></i> The appropriate authority under the Central Government shall review and update from time to time the model HIV and AIDS policy for establishments in accordance with above mentioned rules.</li>
              <li><i class="ri-check-double-line"></i> The model HIV and AIDS policy applicable to an establishment, engaged in the provision of healthcare services and every other establishment where there is a significant risk of occupational exposure to HIV shall provide for a safe working environment and for informed consent for testing, treatment and research in accordance with the provisions of the Act. </li>
              <li><i class="ri-check-double-line"></i> The model HIV and AIDS Policy applicable to an establishment consisting of one hundred or more persons, whether as an employee or officer or member or director or trustee or manager, as the case may be, shall provide for a grievance redressal mechanism in accordance with the provisions of the Act and these rules: Provided that in the case of healthcare establishments, the provisions of this sub-rule shall have the effect as if for the words &ldquo;one hundred or more&rdquo;, the words &ldquo;twenty or more&rdquo; had been substituted. </li>
              <li><i class="ri-check-double-line"></i> The model HIV and AIDS policy as may be applicable and as may be amended and updated from time to time by the appropriate authority under the Central Government shall be adopted by every establishment upon its notification. </li>
